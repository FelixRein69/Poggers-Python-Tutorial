# Methoden sind Unterprogramme, die wiederverwendet werden können. 

# 1) Einführung 
# Beispiel: 
# Wir wollen irgendeine Variable auf der Console ausgeben. Dies tun wir indem wir eine Methode aufrufen,
# die von Python zur Verfügung gestellt wird. 
variable = 10
print(variable)
# ^      ^
# Name   Argument 
# 
# Damit wird die 'variable' auf der Konsole ausgegeben. 

